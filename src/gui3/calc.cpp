#include "calc.h"
#include <iostream>
using namespace std;

double MathLib::plus(double x, double y)
{
    return x + y;
}

double MathLib::minus(double x, double y)
{
    return x - y;
}

double MathLib::multiply(double x, double y)
{
    return x * y;
}

double MathLib::divide(double x, double y)
{
    return x / y;
}
